const num = prompt("Enter a number 0-9");
const isNum = Number(num);

switch (isNum) {
    case 1: 
        alert ("The symbol is ! ");
    break;
    case 2: 
        alert ("The symbol is @ ");
    break;
    case 3: 
        alert ("The symbol is # ");
    break;
    case 4: 
        alert ("The symbol is $ ");
    break;
    case 
        5: alert ("The symbol is % ");
    break;
    case 6: 
        alert ("The symbol is ^ ");
    break;
    case 7: 
        alert ("The symbol is & ");
    break;
    case 8: 
        alert ("The symbol is * ");
    break;
    case 9: 
        alert ("The symbol is ( ");
    break;
    case 0: 
        alert ("The symbol is ) ");
    break;
    default: 
        alert ("Try again later")
}